// an object to convey a single substitution cipher key.
type KeyScheduleItemInt struct {
	KeyShift int
	Comment  string
}

/* ----------------------------------------------------------------
 *				C o n s t r u c t o r s
 *-----------------------------------------------------------------*/

/* ----------------------------------------------------------------
 *				P u b l i c		M e t h o d s
 *-----------------------------------------------------------------*/

// implements fmt.Stringer displaying the KeySchedule object in a single line.
func (ks KeyScheduleItemInt) String() string {
	return fmt.Sprintf("%02d (%s)", ks.KeyShift, ks.Comment)
}
